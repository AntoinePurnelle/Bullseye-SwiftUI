import SwiftUI

extension Color {
    static var accent: Color { return Color("AccentColor") }
    static var background: Color { return Color("BackgroundColor") }
    static var buttonBackground: Color { return Color("ButtonColor") }
    static var buttonFilledBackground: Color { return Color("ButtonFilledBackgroundColor") }
    static var buttonFilledText: Color { return Color("ButtonFilledTextColor") }
    static var buttonStroke: Color { return Color("ButtonStrokeColor") }
    static var text: Color { return Color("TextColor") }
}
